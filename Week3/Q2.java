package Week3;
public class Q2 {
    public static void main(String[] args) {
    
    System.out.print("Square from 1 to 5 are : ");
    for (int i = 1 ; i <= 5; i++) {
        System.out.print((i*i) + " ");
    }
    System.out.println();

    System.out.print("Cube from 1 to 5 are : ");
    for (int i = 1; i <= 5; i++) {
        System.out.print((i*i*i) + " ");
    }       
    }
}
